def reverseString(s):
    # Return the reversed string using slicing
    return s[::-1]

# Example usage
print(reverseString("hello"))                      # Output: "olleh"
print(reverseString("JavaScript"))                 # Output: "tpircSavaJ"
print(reverseString("12345"))                      # Output: "54321"
print(reverseString("A man a plan a canal Panama"))  # Output: "amanaP lanac a nalp a nam A"